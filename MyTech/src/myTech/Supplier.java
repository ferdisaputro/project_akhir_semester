        public void datatable(String string){
            DefaultTableModel tbl=new DefaultTableModel();
            tbl.addColumn("Id Suppier");
            tbl.addColumn("Nama");
            tbl.addColumn("Email");
            tbl.addColumn("Alamat");
            tbl.addColumn("Nomor Telephone");
            table.setModel(tbl);
            Koneksi cn = new Koneksi();
            try{
                Statement statement=cn.GetConnection().createStatement();
                ResultSet res=(ResultSet) statement.executeQuery("select * from supplier");
                while(res.next())
                {
                    tbl.addRow(new Object[]{
                        res.getString("id_supplier"),
                        res.getString("nama"),
                        res.getString("email"),
                        res.getString("alamat"),
                        res.getString("no_telephone"),
                    });
                    table.setModel(tbl);
                }
            }catch (Exception e){
                    JOptionPane.showMessageDialog(rootPane, "error " + e);
            }
        }

